package eclipsproject;

public class ForLoopEx3 {

	public static void main(String[] args) {
	for(int a=1; a<=5; a++) {
		System.out.println("neeraj chauhan");
	}
   System.out.println("**********************");
   System.out.println("^^^^^^^^^^^^^^^^^^^^^");
   for(int a=1; ++a<=5;) {
	   System.out.println("neeraj chauhan");
   }
   System.out.println("**********************");
   System.out.println("^^^^^^^^^^^^^^^^^^^^^");
   for(int a=1 ; a++<=5; a++) {
	   System.out.println("neeraj chauhan");
	   
   }
	}

}
